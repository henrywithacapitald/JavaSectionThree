public class iFElse {
    public static void main(String[] args) {
        int grade = 100;
        if (grade >= 80) {
            System.out.println("you got an A");
        } else if (grade >= 70) {
            System.out.println("your got an B");
        } else if (grade >= 60) {
            System.out.println("you got an C");
        } else if (grade >= 50) {
            System.out.println("you got an D");
        }
        
        
        
        else{
            System.out.println("failed study hard next time!");
        }
    }
}
